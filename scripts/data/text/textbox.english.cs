function textboxTrainingSkill(%skill_str_id, %Value)
{
   %skill_id = skillStrIdToSkillId(%skill_str_id);
   %skill_str = $skill_name[%skill_id];
   
   //make first letter uppercase
   %first_letter = getSubStr(%skill_str, 0, 1);
   %upr_first_letter = strupr(%first_letter);
   %rest = getSubStr(%skill_str, 1);
   %skill_str = %upr_first_letter @ %rest;

	dlgAddTextBoxMessage("\c5" @ %skill_str @ " training increased by " @ %Value @ ".\c1");
}

